    // Método para preencher a tabela
    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblCadastrados.getModel();
        dtm.setRowCount(0);
        UsuarioDAO dao = new UsuarioDAO();
        
        for(Usuario usu : dao.read()) {
            dtm.addRow(new Object[] {
                usu.getId(),
                usu.getNomeUsu(),
                usu.getEmailUsu(),
                usu.getTipoUsu()
            });
        }
    }

