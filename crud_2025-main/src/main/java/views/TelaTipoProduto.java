    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // Construir um objeto do TipoProduto
        usu = new Usuario();
        usu.setNomeUsu(txtNomeUsu.getText());
        usu.setEmailUsu(txtEmailUsu.getText());
        usu.setSenhaUsu(txtSenhaUsu.getText());
        usu.setTipoUsu(txtTipoUsu.getText());
        
        // Invocar o método create da classe DAO
        UsuarioDAO dao = new UsuarioDAO();
        dao.create(usu);
        
        // Limpar os campos e preencher novamente a tabela
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void tblCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastradosMouseClicked
        // TODO add your handling code here:
        int linha = tblCadastrados.getSelectedRow();
        String id = tblCadastrados.getValueAt(linha, 0).toString();
        
        if(linha != -1) {
            UsuarioDAO dao = new UsuarioDAO();
            usu = dao.read(Integer.parseInt(id));
            
            // Preencher os campos do formulário
            txtNomeUsu.setText(usu.getNomeUsu());
            txtEmailUsu.setText(usu.getEmailUsu());
            txtSenhaUsu.setText(usu.getSenhaUsu());
            txtTipoUsu.setText(usu.getTipoUsu());
        }
    }//GEN-LAST:event_tblCadastradosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        usu.setNomeUsu(txtNomeUsu.getText());
        usu.setEmailUsu(txtEmailUsu.getText());
        usu.setSenhaUsu(txtSenhaUsu.getText());
        usu.setTipoUsu(txtTipoUsu.getText());
        
        UsuarioDAO dao = new UsuarioDAO();
        dao.update(usu);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // Confirmar se realmente deseja excluir
        if (usu != null) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este Usuario?") == JOptionPane.YES_OPTION) {
                UsuarioDAO dao = new UsuarioDAO();
                dao.destroy(usu);
                
                limpar();
                preencherTabela();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

